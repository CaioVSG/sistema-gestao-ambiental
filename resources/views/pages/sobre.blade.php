                                    <li style="color: var(--textcolor2); font-size: 16px;">Registro e avaliação de denúncias referentes a potenciais crimes ambientais.</li>
                                    <li style="color: var(--textcolor2); font-size: 16px;">Solicitação de mudas.</li>
                                    <li style="color: var(--textcolor2); font-size: 16px;">Requerimento para poda/supressão.</li>
                                    <li style="color: var(--textcolor2); font-size: 16px;">Requerimento de licenças ambientais com emissão de boletos e submissão eletrônica de documentos.</li>
                                    <li style="color: var(--textcolor2); font-size: 16px;">Consulta de licenças emitidas ou de solicitações por parte de empreendimentos.</li>
                                    <li style="color: var(--textcolor2); font-size: 16px;">Múltiplos perfis para tramitação, análise documental e vistoria dos requerimentos.</li>
                                    <li style="color: var(--textcolor2); font-size: 16px;">Geração de relatórios.</li>
